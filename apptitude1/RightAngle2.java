import java.util.Scanner;


class RightAngle2{
	public static void main(String[] args){
		Scanner sc= new Scanner (System.in);
		System.out.println("Enter The Number of row");
		int row = sc.nextInt();
		for (int i = 1; i<=1; i++){
				for(int k=1;k<=1+i;k++) {
				System.out.print("  ");
			}
			}
		
			for (int j = 1; j<=row ; j++){
				System.out.print( " * ");
		
  				
			System.out.println();
				
                        
		}
	}
}
